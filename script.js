const menu = document.getElementById("menu");

function formatPrice(price) {
    return Number(price).toLocaleString("fa-IR") + " تومان";
}

function createMenu(data) {

    menu.innerHTML = "";

    data.forEach(category => {

        const box = document.createElement("div");
        box.className = "category";

        const header = document.createElement("div");
        header.className = "category-header";

        header.innerHTML = `
            <div>
                <strong>${category.title_fa}</strong><br>
                <small>${category.title_en}</small>
            </div>
            <span class="plus">+</span>
        `;

        const body = document.createElement("div");
        body.className = "category-body";

        category.items
            .filter(item => item.active)
            .forEach(item => {

                const product = document.createElement("div");
                product.className = "item";

                product.innerHTML = `
                    <div class="item-name">
                        <strong>${item.name_fa}</strong>
                        <small>${item.name_en}</small>
                    </div>

                    <div class="price">
                        ${formatPrice(item.price)}
                    </div>
                `;

                body.appendChild(product);

            });

        header.addEventListener("click", () => {

            const opened =
                body.style.display === "block";

            document.querySelectorAll(".category-body")
                .forEach(el => el.style.display = "none");

            document.querySelectorAll(".plus")
                .forEach(el => el.innerHTML = "+");

            if (!opened) {
                body.style.display = "block";
                header.querySelector(".plus").innerHTML = "−";
            }

        });

        box.appendChild(header);
        box.appendChild(body);

        menu.appendChild(box);

    });

}

createMenu(menuData);

